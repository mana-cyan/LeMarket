package com.lemarket.data.reponseObject;

public class TokenString {
    private String token;

    public TokenString() {
        token = "";
    }

    public TokenString(String token) {
        this.token = token;
    }

    public String getToken() {
        return token;
    }

    public void setToken(String token) {
        this.token = token;
    }
}
